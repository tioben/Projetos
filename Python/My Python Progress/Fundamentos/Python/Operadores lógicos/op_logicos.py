print(f"1 {10==4}") #equal
print(f"2 {4!=5}") #not equal
print(f"3 {4>5}") #greater than
print(f"4 {4<5}") #less than
print(f"5 {4>=5}") #greater than or equal to
print(f"6 {4<=5}") #less than or equal to
print(f"7 {(10==4) | (4==4)}") #OR 
print(f"8 {10==4 or 4==4}") #alternative OR
print(f"9 {(10==4) & (4==4)}") #AND
print(f"10 {10==4 and 4==4}") #alternative AND
print(f"11 {(10==4) ^ (4==4)}") #XOR 
print(f"12 {~(4==4)}") #NOT
print(f"13 {not 4==4}") #alternative NOT

